#!/usr/bin/env python3
"""
TDX local data import tool for Kronos fine-tuning.

Reads local TDX (通达信) historical K-line data and converts it to the Kronos
6-field format (open, high, low, close, vol, amt) for model fine-tuning.

Features:
- Direct import of daily (1d) and 5-min (5m) from TDX local files
- Other periods (1m, 15m, 30m, 60m, 1w, 1M) generated by resampling
- Configurable price adjustment with automatic factor fetching
- Amount formula: prefer original data, fallback to mean(OHLC) * vol
- Historical data continuity analysis with gap reporting
"""

