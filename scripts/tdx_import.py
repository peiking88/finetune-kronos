def parse_args():
    p = argparse.ArgumentParser(
        description="TDX local data import tool for Kronos fine-tuning"
    )

    p.add_argument(
        "--tdxdir",
        default=DEFAULT_TDXDIR,
        help=f"TDX data directory (default: {DEFAULT_TDXDIR})",
    )
    p.add_argument(
        "--symbols",
        nargs="*",
        help="Stock codes to import (e.g. sh600000 sz000001). "
             "If omitted, auto-discover all stocks.",
    )
    p.add_argument(
        "--symbol-file",
        help="File with stock codes, one per line",
    )
    p.add_argument(
        "--periods",
        nargs="+",
        default=["1d"],
        choices=list(PERIOD_CONFIG.keys()),
        help="Periods to import (default: 1d)",
    )
    p.add_argument(
        "--dividend-type",
        default="none",
        choices=["none", "front", "back"],
        help="Price adjustment type: front (qfq), back (hfq), none (default)",
    )
    p.add_argument(
        "--output-dir",
        default="./data/tdx_import",
        help="Output directory (default: ./data/tdx_import)",
    )
    p.add_argument(
        "--start-date",
        help="Start date filter (YYYY-MM-DD)",
    )
    p.add_argument(
        "--end-date",
        help="End date filter (YYYY-MM-DD)",
    )
    p.add_argument(
        "--no-split",
        action="store_true",
        help="Save as single file instead of train/val/test splits",
    )
    p.add_argument(
        "--train-range",
        nargs=2,
        default=["2011-01-01", "2022-12-31"],
        metavar=("START", "END"),
        help="Training set date range (default: 2011-01-01 2022-12-31)",
    )
    p.add_argument(
        "--val-range",
        nargs=2,
        default=["2022-09-01", "2024-06-30"],
        metavar=("START", "END"),
        help="Validation set date range (default: 2022-09-01 2024-06-30)",
    )
    p.add_argument(
        "--test-range",
        nargs=2,
        default=["2024-04-01", "2025-06-05"],
        metavar=("START", "END"),
        help="Test set date range (default: 2024-04-01 2025-06-05)",
    )
    p.add_argument(
        "--no-continuity",
        action="store_true",
        help="Skip data continuity check",
    )
    p.add_argument(
        "--limit",
        type=int,
        default=0,
        help="Limit to first N stocks (for testing, 0 = all)",
    )
    p.add_argument(
        "--list-stocks",
        action="store_true",
        help="List available stocks and exit",
    )

    return p.parse_args()


def load_symbols(args) -> list[str]:
    """Resolve stock list from CLI args."""
    if args.symbols:
        return args.symbols

    if args.symbol_file:
        with open(args.symbol_file) as f:
            return [line.strip() for line in f if line.strip()]

    # Auto-discover
    importer = TdxDataImporter(tdxdir=args.tdxdir)
    stocks = importer.discover_stocks()
    print(f"Auto-discovered {len(stocks)} stocks")
    return stocks


def main():
    args = parse_args()

    # List stocks mode
    if args.list_stocks:
        importer = TdxDataImporter(tdxdir=args.tdxdir)
        stocks = importer.discover_stocks()
        print(f"Found {len(stocks)} stocks:")
        for s in stocks:
            print(f"  {s}")
        return

    # Load symbols
    stock_list = load_symbols(args)
    if not stock_list:
        print("ERROR: No stocks specified or discovered.")
        sys.exit(1)

    if args.limit > 0:
        stock_list = stock_list[: args.limit]
        print(f"Limited to first {args.limit} stocks")

    # Configure splits
    splits = None
    if not args.no_split:
        splits = {
            "train": tuple(args.train_range),
            "val": tuple(args.val_range),
            "test": tuple(args.test_range),
        }

    # Run import
    importer = TdxDataImporter(
        tdxdir=args.tdxdir,
        dividend_type=args.dividend_type,
        start_date=args.start_date,
        end_date=args.end_date,
    )

    for period in args.periods:
        dataset = importer.build_dataset(
            stock_list=stock_list,
            period=period,
            check_continuity=not args.no_continuity,
        )

        if not dataset:
            print(f"  SKIP: No data for period {period}")
            continue

        importer.save_dataset(
            dataset=dataset,
            output_dir=args.output_dir,
            period=period,
            splits=splits,
        )

    # Continuity report
    if not args.no_continuity:
        importer.print_gap_report()

    print(f"\nDone. Output saved to {args.output_dir}/")
    print("To use with Kronos, update finetune/config.py:")
    print(f"  self.dataset_path = \"{os.path.abspath(args.output_dir)}/1d\"")


if __name__ == "__main__":
    main()
