# The 6-field format used by Kronos project
FEATURE_COLUMNS = ["open", "high", "low", "close", "vol", "amt"]

# Period configuration: how to obtain each supported period
PERIOD_CONFIG = {
    "1m":   {"source": "direct", "method": "minute_1"},
    "5m":   {"source": "direct", "method": "minute_5"},
    "1d":   {"source": "direct", "method": "daily"},
    "15m":  {"source": "resample", "base": "5m",  "rule": "15min"},
    "30m":  {"source": "resample", "base": "5m",  "rule": "30min"},
    "60m":  {"source": "resample", "base": "5m",  "rule": "1h"},
    "1w":   {"source": "resample", "base": "1d",  "rule": "W-FRI"},
    "1M":   {"source": "resample", "base": "1d",  "rule": "1ME"},
}

# Default TDX data directory (wine prefix)
DEFAULT_TDXDIR = os.path.expanduser("~/.local/share/tdxcfv/drive_c/tc/")

# Resample aggregation rules
OHLCV_AGG = {
    "open": "first",
    "high": "max",
    "low": "min",
    "close": "last",
    "volume": "sum",
    "amount": "sum",
}

# TDX column mapping to Kronos 6-field format
TDX_TO_KRONOS = {
    "volume": "vol",
    "vol": "vol",
    "amount": "amt",
    "amt": "amt",
}


