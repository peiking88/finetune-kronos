def check_daily_continuity(
    df: pd.DataFrame, symbol: str, min_date=None, max_date=None
) -> list[dict]:
    """
    Check daily data for missing trading days.

    A gap is defined as >= 2 consecutive weekdays without data.
    Returns list of gap records: {symbol, start, end, days_missing, pct}.
    """
    if df.empty:
        return []

    date_col = "date" if "date" in df.columns else df.index.name
    if date_col and date_col != "date":
        dates = pd.to_datetime(df.index)
    else:
        dates = pd.to_datetime(df["date"]) if "date" in df.columns else pd.to_datetime(df.index)

    dates = sorted(dates.dropna().unique())

    if len(dates) < 2:
        return []

    d0 = pd.Timestamp(min_date) if min_date else dates[0]
    d1 = pd.Timestamp(max_date) if max_date else dates[-1]

    # Build expected weekday calendar (Mon-Fri)
    expected = pd.bdate_range(start=d0, end=d1, freq="C", weekmask="Mon Tue Wed Thu Fri")
    observed = set(pd.Timestamp(d).date() for d in dates)
    expected_set = set(d.date() for d in expected)

    missing = sorted(expected_set - observed)
    if not missing:
        return []

    gaps = _group_consecutive_dates(missing, symbol, dates[0], dates[-1], d0, d1)
    return gaps


def _group_consecutive_dates(missing, symbol, first_date, last_date, d0, d1):
    """Group a sorted list of missing dates into consecutive gap records."""
    gaps = []
    span_days = (d1 - d0).days or 1

    start = missing[0]
    end = missing[0]

    for i in range(1, len(missing)):
        if (missing[i] - missing[i - 1]).days == 1:
            end = missing[i]
        else:
            days = (end - start).days + 1
            gaps.append({
                "symbol": symbol,
                "start": str(start),
                "end": str(end),
                "days_missing": days,
                "pct": round(100 * days / span_days, 2),
            })
            start = end = missing[i]

    days = (end - start).days + 1
    gaps.append({
        "symbol": symbol,
        "start": str(start),
        "end": str(end),
        "days_missing": days,
        "pct": round(100 * days / span_days, 2),
    })

    return gaps


def check_intraday_continuity(
    df: pd.DataFrame, symbol: str, expected_interval_minutes: int = 5
) -> list[dict]:
    """
    Check intraday data for gaps > expected interval within each trading day.

    Returns list of gap records per trading day.
    """
    if df.empty:
        return []

    date_col = "date" if "date" in df.columns else None
    if date_col and date_col in df.columns:
        df = df.copy()
        df["_dt"] = pd.to_datetime(df[date_col])
    else:
        df = df.copy()
        df["_dt"] = pd.to_datetime(df.index)

    df["_day"] = df["_dt"].dt.date
    gaps = []

    for day, grp in df.groupby("_day"):
        times = sorted(grp["_dt"].dropna().unique())
        if len(times) < 2:
            continue

        for i in range(1, len(times)):
            diff = (times[i] - times[i - 1]).total_seconds() / 60
            if diff > expected_interval_minutes * 1.5:
                gaps.append({
                    "symbol": symbol,
                    "date": str(day),
                    "start": str(times[i - 1]),
                    "end": str(times[i]),
                    "gap_minutes": round(diff, 1),
                })

    return gaps


def summarize_gaps(all_gaps: dict[str, list]) -> str:
    """Generate a human-readable gap summary report."""
    if not all_gaps:
        return "No gaps detected. Data is continuous."

    lines = ["=" * 60, "DATA CONTINUITY REPORT", "=" * 60]

    total_stocks_affected = 0
    total_gaps = 0
    total_missing_days = 0

    for period, gaps in sorted(all_gaps.items()):
        if not gaps:
            continue
        total_stocks_affected += 1
        p_total_gaps = len(gaps)
        total_gaps += p_total_gaps

        lines.append(f"\n--- Period: {period} ({p_total_gaps} gaps) ---")

        # Aggregate by symbol
        by_symbol = defaultdict(list)
        for g in gaps:
            by_symbol[g["symbol"]].append(g)

        for sym, sym_gaps in sorted(by_symbol.items()):
            if "days_missing" in sym_gaps[0]:
                days = sum(g["days_missing"] for g in sym_gaps)
                total_missing_days += days
                lines.append(
                    f"  {sym}: {len(sym_gaps)} gap(s), {days} missing days total"
                )
                for g in sym_gaps[:3]:  # Show first 3
                    lines.append(
                        f"    {g['start']} ~ {g['end']}: "
                        f"{g['days_missing']} days ({g['pct']}%)"
                    )
                if len(sym_gaps) > 3:
                    lines.append(f"    ... and {len(sym_gaps) - 3} more gap(s)")
            else:
                lines.append(
                    f"  {sym}: {len(sym_gaps)} intraday gap(s)"
                )

    lines.append(f"\n{'=' * 60}")
    lines.append(
        f"Summary: {total_gaps} gaps across {total_stocks_affected} periods"
    )
    if total_missing_days:
        lines.append(f"Total missing trading days: {total_missing_days}")
    lines.append("=" * 60)

    return "\n".join(lines)


