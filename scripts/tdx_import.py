class TdxDataImporter:
    """
    Import TDX local K-line data and convert to Kronos 6-field format.

    Parameters
    ----------
    tdxdir : str, optional
        Path to TDX data directory.
    dividend_type : str
        Price adjustment type: 'front' (qfq), 'back' (hfq), or 'none'.
    start_date : str, optional
        Filter start date (YYYY-MM-DD).
    end_date : str, optional
        Filter end date (YYYY-MM-DD).
    """

    def __init__(
        self,
        tdxdir: Optional[str] = None,
        dividend_type: str = "none",
        start_date: Optional[str] = None,
        end_date: Optional[str] = None,
    ):
        self.tdxdir = tdxdir or DEFAULT_TDXDIR
        self.dividend_type = dividend_type
        self.start_date = start_date
        self.end_date = end_date
        self._reader: Optional[Reader] = None

        # Adjust type for tdxdata (qfq / hfq / None)
        self._adjust_code = ADJUST_MAP.get(dividend_type)

        # Factor cache: {code: DataFrame} persisted to disk
        self._factor_cache: dict[str, pd.DataFrame] = {}
        self._factor_cache_dir = os.path.join(
            os.path.dirname(self.tdxdir), ".factor_cache"
        )

        # Gap records: period -> list of gap dicts
        self.gaps: dict[str, list[dict]] = defaultdict(list)

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def discover_stocks(self, markets=("sh", "sz", "bj")) -> list[str]:
        """Discover available stock codes from TDX daily data directory."""
        stocks = []
        for market in markets:
            lday_dir = os.path.join(self.tdxdir, "vipdoc", market, "lday")
            if not os.path.isdir(lday_dir):
                continue
            for fname in os.listdir(lday_dir):
                if fname.endswith(".day") and fname[:2] == market:
                    code = fname.replace(".day", "")
                    stocks.append(code)
        return sorted(stocks)

    def import_period(
        self,
        stock_list: list[str],
        period: str = "1d",
    ) -> pd.DataFrame:
        """
        Import a single period for multiple stocks.

        Returns DataFrame with columns:
        stock_code, date, open, high, low, close, vol, amt

        Parameters
        ----------
        stock_list : list[str]
            Stock codes like ['sh600000', 'sz000001'].
        period : str
            Target period key from PERIOD_CONFIG.
        """
        config = PERIOD_CONFIG.get(period)
        if config is None:
            raise ValueError(
                f"Unsupported period '{period}'. "
                f"Supported: {sorted(PERIOD_CONFIG.keys())}"
            )

        if config["source"] == "direct":
            return self._import_direct(stock_list, config)
        else:
            return self._import_resampled(stock_list, period, config)

    def build_dataset(
        self,
        stock_list: list[str],
        period: str = "1d",
        check_continuity: bool = True,
    ) -> dict[str, pd.DataFrame]:
        """
        Build the full {symbol: DataFrame} dataset for a period.

        Each DataFrame has datetime index and 6 Kronos columns.
        """
        print(f"\n{'=' * 60}")
        print(f"Importing period: {period}")
        print(f"Stocks: {len(stock_list)}")

        df_all = self.import_period(stock_list, period)
        if df_all.empty:
            print(f"  WARNING: No data imported for period {period}")
            return {}

        # Convert to Kronos 6-field format per symbol
        dataset = {}
        for code, grp in df_all.groupby("stock_code"):
            symbol_df = self._to_kronos_format(grp)
            if symbol_df is None or symbol_df.empty:
                continue
            dataset[code] = symbol_df

        # Continuity check
        if check_continuity and period == "1d":
            for sym, sdf in dataset.items():
                gaps = check_daily_continuity(
                    sdf, sym, self.start_date, self.end_date
                )
                if gaps:
                    self.gaps[period].extend(gaps)

            if self.gaps.get(period):
                print(f"  Found {len(self.gaps[period])} daily gap(s)")

        elif check_continuity and period in ("1m", "5m"):
            interval = 1 if period == "1m" else 5
            for sym, sdf in dataset.items():
                gaps = check_intraday_continuity(sdf, sym, interval)
                if gaps:
                    self.gaps[period].extend(gaps)

        # Print per-symbol data range summary
        if dataset:
            ranges = []
            for sym, sdf in dataset.items():
                ranges.append((sym, sdf.index[0], sdf.index[-1], len(sdf)))
            ranges.sort(key=lambda x: x[1])  # Sort by start date
            print(f"  Exported {len(dataset)} symbols")
            print(f"  Data range: {ranges[0][1].strftime('%Y-%m-%d')} ~ "
                  f"{ranges[-1][2].strftime('%Y-%m-%d')} "
                  f"(across {len(dataset)} symbols)")
        return dataset

    def save_dataset(
        self,
        dataset: dict[str, pd.DataFrame],
        output_dir: str,
        period: str = "1d",
        splits: Optional[dict[str, tuple[str, str]]] = None,
    ):
        """
        Save dataset to disk as pickle file(s).

        If splits is provided, data is split by date range and saved as
        separate files (train/val/test). Otherwise saved as single file.

        Parameters
        ----------
        dataset : dict
            {symbol: DataFrame} mapping.
        output_dir : str
            Output directory.
        period : str
            Period key, used as subdirectory name.
        splits : dict, optional
            {name: (start_date, end_date)} for train/val/test splits.
        """
        period_dir = os.path.join(output_dir, period)
        os.makedirs(period_dir, exist_ok=True)

        if splits:
            for split_name, (s_start, s_end) in splits.items():
                split_data = {}
                for sym, sdf in dataset.items():
                    mask = (sdf.index >= s_start) & (sdf.index <= s_end)
                    subset = sdf[mask]
                    if not subset.empty:
                        split_data[sym] = subset

                fpath = os.path.join(period_dir, f"{split_name}_data.pkl")
                with open(fpath, "wb") as f:
                    pickle.dump(split_data, f)
                if len(split_data) == 0:
                    print(f"  WARNING: {split_name} split is empty! "
                          f"Date range ({s_start}, {s_end}) may not overlap "
                          f"with available data. Adjust --{split_name}-range "
                          f"or remove --start-date/--end-date to import all data.")
                else:
                    print(f"  Saved {fpath} ({len(split_data)} symbols)")
        else:
            fpath = os.path.join(period_dir, "data.pkl")
            with open(fpath, "wb") as f:
                pickle.dump(dataset, f)
            print(f"  Saved {fpath} ({len(dataset)} symbols)")

    def print_gap_report(self):
        """Print the accumulated gap analysis report."""
        print(summarize_gaps(self.gaps))

    def get_gap_summary(self) -> dict:
        """Return structured gap summary for programmatic use."""
        summary = {}
        for period, gaps in self.gaps.items():
            summary[period] = {
                "total_gaps": len(gaps),
                "affected_symbols": len(set(g["symbol"] for g in gaps)),
                "details": gaps,
            }
        return summary

    # ------------------------------------------------------------------
    # Internal: data reading
    # ------------------------------------------------------------------

    def _get_reader(self) -> Reader:
        if self._reader is None:
            if not os.path.isdir(self.tdxdir):
                raise FileNotFoundError(
                    f"TDX directory not found: {self.tdxdir}. "
                    f"Specify --tdxdir or ensure default path exists."
                )
            self._reader = Reader.factory(market="std", tdxdir=self.tdxdir)
        return self._reader

    def _import_direct(
        self, stock_list: list[str], config: dict
    ) -> pd.DataFrame:
        """Import directly from TDX local files."""
        reader = self._get_reader()
        method = config["method"]
        result_parts = []

        for i, code in enumerate(stock_list):
            if (i + 1) % 200 == 0:
                print(f"  Reading... {i + 1}/{len(stock_list)}")

            try:
                df = self._fetch_single(reader, code, method)
                if df is not None and not df.empty:
                    if method == "daily" and self._adjust_code:
                        df = self._adjust_with_cache(df, code)
                    result_parts.append(df)
            except Exception as e:
                print(f"  WARNING: Failed to read {code}: {e}")

        if not result_parts:
            return pd.DataFrame()

        df_all = pd.concat(result_parts, ignore_index=True)

        # Date filtering
        if self.start_date or self.end_date:
            if "date" in df_all.columns:
                date_series = pd.to_datetime(df_all["date"], errors="coerce")
                if self.start_date:
                    df_all = df_all[date_series >= pd.Timestamp(self.start_date)]
                if self.end_date:
                    df_all = df_all[date_series <= pd.Timestamp(self.end_date)]

        return df_all

    def _import_resampled(
        self, stock_list: list[str], period: str, config: dict
    ) -> pd.DataFrame:
        """Generate period by resampling from a base period."""
        base_period = config["base"]
        rule = config["rule"]

        print(f"  Resampling from {base_period} -> {period} (rule={rule})")

        # Import base data first
        base_config = PERIOD_CONFIG[base_period]
        df_base = self._import_direct(stock_list, base_config)
        if df_base.empty:
            return pd.DataFrame()

        result_parts = []
        for code, grp in df_base.groupby("stock_code"):
            try:
                resampled = self._resample_ohlcv(grp, rule)
                if resampled is not None and not resampled.empty:
                    resampled["stock_code"] = code
                    result_parts.append(resampled)
            except Exception as e:
                print(f"  WARNING: Failed to resample {code}: {e}")

        if not result_parts:
            return pd.DataFrame()
        return pd.concat(result_parts, ignore_index=True)

    def _fetch_single(
        self, reader: Reader, code: str, method: str
    ) -> Optional[pd.DataFrame]:
        """Read a single stock from TDX local files."""
        if method == "daily":
            df = reader.daily(symbol=code)
        elif method == "minute_1":
            df = reader.minute(symbol=code, suffix=1)
        elif method == "minute_5":
            df = reader.fzline(symbol=code)
        else:
            return None

        if df is None or df.empty:
            return None

        df = df.copy()

        # Normalize index to 'date' column
        if df.index.name in ("date", "datetime"):
            df.reset_index(inplace=True)

        df["stock_code"] = code

        # Ensure date column
        if "date" not in df.columns and "datetime" in df.columns:
            df["date"] = pd.to_datetime(df["datetime"], errors="coerce")
        elif "date" in df.columns:
            df["date"] = pd.to_datetime(df["date"], errors="coerce")

        return df

    def _adjust_with_cache(
        self, df: pd.DataFrame, code: str
    ) -> pd.DataFrame:
        """Apply price adjustment with disk-cached factors."""
        if code in self._factor_cache:
            factor_df = self._factor_cache[code]
            return self._apply_factor(df, factor_df)

        cache_file = os.path.join(self._factor_cache_dir, f"{code}.pkl")
        if os.path.exists(cache_file):
            try:
                factor_df = pd.read_pickle(cache_file)
                self._factor_cache[code] = factor_df
                return self._apply_factor(df, factor_df)
            except Exception:
                pass

        try:
            df_adjusted = apply_adjust(df, code, self._adjust_code)
            from tdxdata.sources.adjust import fetch_factor
            factor_df = fetch_factor(code, self._adjust_code)
            if factor_df is not None and not factor_df.empty:
                os.makedirs(self._factor_cache_dir, exist_ok=True)
                factor_df.to_pickle(cache_file)
                self._factor_cache[code] = factor_df
            return df_adjusted
        except Exception:
            return df

    @staticmethod
    def _apply_factor(df: pd.DataFrame, factor_df: pd.DataFrame) -> pd.DataFrame:
        """Apply cached factor DataFrame to OHLC columns."""
        df = df.copy()
        date_col = "date" if "date" in df.columns else "datetime"
        if date_col not in df.columns:
            return df
        df[date_col] = pd.to_datetime(df[date_col])
        factor_df = factor_df.copy()
        factor_df.index = pd.to_datetime(factor_df.index)
        factor_df = factor_df.sort_index()
        first_val = factor_df["factor"].iloc[0]
        last_val = factor_df["factor"].iloc[-1]
        direction = "backward" if last_val > first_val else "forward"
        merged = pd.merge_asof(
            df.sort_values(date_col).reset_index(drop=True),
            factor_df[["factor"]],
            left_on=date_col,
            right_index=True,
            direction=direction,
        )
        merged["factor"] = merged["factor"].ffill().bfill().fillna(1.0)
        for col in ["open", "high", "low", "close"]:
            if col in merged.columns:
                merged[col] = merged[col] * merged["factor"]
        merged = merged.drop(columns=["factor"], errors="ignore")
        return merged

    # ------------------------------------------------------------------
    # Internal: resampling
    # ------------------------------------------------------------------

    @staticmethod
    def _resample_ohlcv(df: pd.DataFrame, rule: str) -> Optional[pd.DataFrame]:
        """Resample OHLCV data to a new frequency."""
        df = df.copy()
        df["date"] = pd.to_datetime(df["date"], errors="coerce")
        df = df.set_index("date").sort_index()

        # Ensure numeric types
        for col in ["open", "high", "low", "close"]:
            if col in df.columns:
                df[col] = pd.to_numeric(df[col], errors="coerce")
        for col in ["volume", "amount"]:
            if col in df.columns:
                df[col] = pd.to_numeric(df[col], errors="coerce").fillna(0)

        # Only resample rows that have OHLC
        ohlc_cols = [c for c in ["open", "high", "low", "close"] if c in df.columns]
        if len(ohlc_cols) < 4:
            return None

        valid_mask = df[ohlc_cols].notna().all(axis=1)
        if not valid_mask.any():
            return None

        resampled = df[valid_mask].resample(rule).agg(OHLCV_AGG)

        # Remove rows with all-NaN OHLC
        resampled = resampled.dropna(subset=ohlc_cols, how="all")
        resampled = resampled.reset_index()

        return resampled

    # ------------------------------------------------------------------
    # Internal: format conversion
    # ------------------------------------------------------------------

    def _to_kronos_format(self, df: pd.DataFrame) -> Optional[pd.DataFrame]:
        """
        Convert a single-stock TDX DataFrame to Kronos 6-field format.

        Returns DataFrame with datetime index and columns:
        open, high, low, close, vol, amt
        """
        df = df.copy()

        # Date handling
        if "date" in df.columns:
            df["date"] = pd.to_datetime(df["date"], errors="coerce")
            df = df.dropna(subset=["date"])
            df = df.set_index("date")
        elif df.index.name in ("date", "datetime"):
            df.index = pd.to_datetime(df.index, errors="coerce")

        df = df.sort_index()

        # Normalize column names
        # Map TDX names to Kronos names
        rename = {}
        for col in df.columns:
            if col in ("open", "high", "low", "close"):
                pass  # Keep as-is
            elif col == "volume":
                rename["volume"] = "vol"
            elif col == "amount":
                rename["amount"] = "amt"
            elif col in ("vol", "amt"):
                pass

        df = df.rename(columns=rename)

        # Ensure numeric types
        for col in ["open", "high", "low", "close"]:
            if col in df.columns:
                df[col] = pd.to_numeric(df[col], errors="coerce")

        if "vol" in df.columns:
            df["vol"] = pd.to_numeric(df["vol"], errors="coerce").fillna(0)
        if "amt" in df.columns:
            df["amt"] = pd.to_numeric(df["amt"], errors="coerce").fillna(0)

        # Compute amount if missing or all-zero
        need_amt = True
        if "amt" in df.columns:
            if df["amt"].notna().any() and (df["amt"] > 0).any():
                need_amt = False

        if need_amt:
            ohlc_cols = ["open", "high", "low", "close"]
            if all(c in df.columns for c in ohlc_cols) and "vol" in df.columns:
                mean_ohlc = df[ohlc_cols].mean(axis=1)
                df["amt"] = mean_ohlc * df["vol"]
            else:
                df["amt"] = 0.0

        # Keep only Kronos 6 columns
        keep = [c for c in FEATURE_COLUMNS if c in df.columns]
        df = df[keep]

        # Drop rows where all OHLC are NaN
        ohlc = [c for c in ["open", "high", "low", "close"] if c in df.columns]
        if ohlc:
            df = df.dropna(subset=ohlc, how="all")

        if df.empty:
            return None

        return df


